class DataBindingActivity : AppCompatActivity() {
    private lateinit var myViewModel: DataBindingViewModel
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        title = "DataBinding学习"
        val binding =
            DataBindingUtil.setContentView<ActivityMainDataBindingBinding>(
                this,
                R.layout.activity_main_data_binding
            )
        myViewModel = ViewModelProvider(this).get(DataBindingViewModel::class.java)
        //绑定视图数据
        binding.dataBindingModel = myViewModel
        binding.lifecycleOwner = this
    }
}