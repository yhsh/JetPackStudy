class MyListAdapter(private val isViewPager: Boolean) :
    ListAdapter<Int, RecyclerView.ViewHolder>(CallBack) {
    object CallBack : DiffUtil.ItemCallback<Int>() {
        override fun areItemsTheSame(oldItem: Int, newItem: Int): Boolean {
            return oldItem == newItem
        }

        override fun areContentsTheSame(oldItem: Int, newItem: Int): Boolean {
            return oldItem == newItem
        }
    }

    override fun onCreateViewHolder(parent: ViewGroup, viewType: Int): RecyclerView.ViewHolder {
        val view = LayoutInflater.from(parent.context).inflate(R.layout.adapter_view, parent, false)
        if (isViewPager) {
            view.layoutParams.height = ViewGroup.LayoutParams.MATCH_PARENT
            view.ivShowImg.layoutParams.height = ViewGroup.LayoutParams.MATCH_PARENT
            view.ivShowImg.layoutParams.width = ViewGroup.LayoutParams.MATCH_PARENT
        }
        return object : RecyclerView.ViewHolder(view) {}
    }

    override fun onBindViewHolder(holder: RecyclerView.ViewHolder, position: Int) {
        holder.itemView.ivShowImg.setImageResource(getItem(position))
    }
}